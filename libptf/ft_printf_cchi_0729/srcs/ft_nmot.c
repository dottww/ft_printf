#include <string.h>

int			ft_nmot(char const *s, char c)
{
	size_t	i;
	size_t	n_s;

	i = 0;
	n_s = 0;
	while (s[i])
	{
		if (i == 0 && s[i] != c)
			n_s++;
		if (s[i] == c && s[i + 1] != c && s[i + 1] != '\0')
			n_s++;
		i++;
	}
	return (n_s);
}
