#include <string.h>

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;
	char	*s2_s2;

	i = 0;
	j = 0;
	s2_s2 = (char*)s2;
	while (s1[i] != '\0')
		i++;
	if (s2_s2[j] == 0)
		return (s1);
	while (n > j && s2_s2[j])
	{
		s1[i + j] = s2_s2[j];
		j++;
	}
	s1[i + j] = '\0';
	return (s1);
}
