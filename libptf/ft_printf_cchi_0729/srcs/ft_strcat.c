char		*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		e;
	char	*s2_s2;

	i = 0;
	e = 0;
	s2_s2 = (char*)s2;
	while (s1[e])
		e++;
	while (s2_s2[i])
	{
		s1[e + i] = s2_s2[i];
		i++;
	}
	s1[e + i] = '\0';
	return (s1);
}
