#include <stdlib.h>

char		*ft_itoa(int n)
{
	int		nb;
	char	*str;
	int		q;

	q = 1;
	nb = n;
	while (n < -9 || n > 9)
	{
		n = n / 10;
		q++;
	}
	q = (n < 0 ? q + 1 : q);
	if (!(str = (char*)malloc(sizeof(char) * (q + 1))))
		return (0);
	n = (n > 0 ? -n : n);
	str[0] = (n < 0 ? '-' : 0);
	str[q--] = '\0';
	str[q--] = (nb < 0 ? -(nb % 10) + 48 : nb % 10 + 48);
	nb = nb / 10;
	while (nb != 0 && q >= 0)
	{
		str[q--] = (nb < 0 ? -(nb % 10) + 48 : nb % 10 + 48);
		nb = nb / 10;
	}
	return (str);
}
