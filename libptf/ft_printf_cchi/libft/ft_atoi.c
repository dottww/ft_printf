int	ft_atoi(const char *str)
{
	int nb;
	int ng;
	int i;

	nb = 0;
	ng = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			ng = 1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 - (str[i] - 48);
		i++;
	}
	if (ng == 1)
		return (nb);
	return (-nb);
}
