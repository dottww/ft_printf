char		*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*src_src;

	i = 0;
	src_src = (char*)src;
	while (src_src[i])
	{
		dst[i] = src_src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
